// Levenshtein distance, used only as a last-resort fallback to catch
// small typos ("Grmany", "Austrailia") against the full country name
// list above. Kept tiny and dependency-free.
function levenshtein(a, b) {
  const m = a.length;
  const n = b.length;
  if (m === 0) return n;
  if (n === 0) return m;

  const prev = new Array(n + 1);
  const curr = new Array(n + 1);

  for (let j = 0; j <= n; j += 1) prev[j] = j;

  for (let i = 1; i <= m; i += 1) {
    curr[0] = i;
    for (let j = 1; j <= n; j += 1) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1;
      curr[j] = Math.min(
        prev[j] + 1,
        curr[j - 1] + 1,
        prev[j - 1] + cost,
      );
    }
    for (let j = 0; j <= n; j += 1) prev[j] = curr[j];
  }

  return prev[n];
}

const ALL_ALIAS_KEYS = Object.keys(COUNTRY_ALIASES);

function fuzzyMatchAlpha2(input) {
  let best = null;
  let bestDistance = Infinity;

  ALL_ALIAS_KEYS.forEach((key) => {
    if (key.length <= 3) return;

    const distance = levenshtein(input, key);
    const threshold = key.length <= 5 ? 1 : 2;

    if (distance <= threshold && distance < bestDistance) {
      bestDistance = distance;
      best = COUNTRY_ALIASES[key];
    }
  });

  return best;
}

function resolveCountry(rawCountry) {
  const trimmed = String(rawCountry || "").trim();

  if (!trimmed) return null;

  const key = trimmed.toLowerCase();

  const directCode = COUNTRY_ALIASES[key] || fuzzyMatchAlpha2(key);

  if (!directCode) return null;

  return { code: directCode, name: ALPHA2_TO_NAME[directCode] || trimmed };
}

function ClinicalSitesMap({ sites = [] }) {
  const { chartData, unresolved } = useMemo(() => {
    const totals = {};
    const unmatched = new Set();

    sites.forEach((site) => {
      const rawCountry = site.country;
      const resolved = resolveCountry(rawCountry);

      if (!resolved) {
        const trimmed = String(rawCountry || "").trim();
        if (trimmed) unmatched.add(trimmed);
        return;
      }

      totals[resolved.code] =
        (totals[resolved.code] || 0) + Number(site.enrolled || 0);
    });

    const data = [["Country", "Enrolled"]];

    Object.entries(totals).forEach(([code, enrolled]) => {
      data.push([code, enrolled]);
    });

    return { chartData: data, unresolved: Array.from(unmatched) };
  }, [sites]);

  const options = {
    colorAxis: {
      colors: ["#dbeafe", "#2563eb", "#0f2f6b"],
    },
    backgroundColor: "#ffffff",
    datalessRegionColor: "#eeeeee",
    defaultColor: "#eeeeee",
    legend: {
      textStyle: {
        fontSize: 12,
      },
    },
    tooltip: {
      textStyle: {
        fontSize: 13,
      },
    },
  };

  if (chartData.length === 1) {
    return (
      <div
        style={{
          height: 420,
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
          color: "#666",
        }}
      >
        No countries available.
      </div>
    );
  }

  return (
    <div>
      <Chart
        chartType="GeoChart"
        width="100%"
        height="450px"
        data={chartData}
        options={options}
      />

      {unresolved.length > 0 && (
        <p style={{ fontSize: 12, color: "#888", marginTop: 8 }}>
          Could not place on the map: {unresolved.join(", ")}. Check the
          spelling of the country field for these sites.
        </p>
      )}
    </div>
  );
}

export default ClinicalSitesMap;